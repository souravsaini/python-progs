#!/usr/bin/python3

var1 = 10
var2 = 11

# Relational Operators

# Less than (<)
print(var1 < var2)

# Greater tahn (>)
print(var1 > var2)

# Less than or equals to (<=)
print(var1 <= var2)

# Greater than or equals to (>=)
print(var1 >= var2)

# Comparison Equal to ==
print(var1 == var2)

# Comparison Not Equal to (!=)
print(var1 != var2)